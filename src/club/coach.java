    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        //===================stor selected row ID ===================
       int cid = Integer.parseInt(coachTable.getValueAt(coachTable.getSelectedRow(), 0).toString());
       //user confirmation
       int update= JOptionPane.showConfirmDialog(null, "Confirm to Update Coach Information.","warning",JOptionPane.YES_NO_OPTION);
       
       //if yes execute
       if (update==JOptionPane.YES_OPTION){
            try {
                Class.forName("com.mysql.jdbc.Driver");
                sqlconn = DriverManager.getConnection(dataconn, username, password);
                pst = sqlconn.prepareStatement("update coach set cname=?,gender=?,sid=(select sid from sport where sport.sname=?),exp=?,salary=? where cid=? ");
                
                pst.setString(1, jTextFieldName.getText());
                pst.setString(2, (String) jComboBoxGender.getSelectedItem());
                pst.setString(3, (String) jComboBoxSport.getSelectedItem());
                pst.setString(4, jTextFieldExperience.getText());
                pst.setString(5, jTextFieldsalary.getText());
                pst.setInt(6, cid);
                
                pst.executeUpdate();
                sqlconn.close();
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(coach.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(coach.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            //======update BD=====
            UpdateDB();
            ClearTextFields();
            
        }
       
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    
