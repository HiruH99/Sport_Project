 //===========================Function to show and update content on table=========================
    public void UpdateDB(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            sqlconn = DriverManager.getConnection(dataconn, username, password);
            pst = sqlconn.prepareStatement("select cid,cname,gender,sname,exp,salary from coach,sport where sport.sid=coach.sid");
            
            RS=pst.executeQuery();
            ResultSetMetaData StData = RS.getMetaData();
            
            int q = StData.getColumnCount();
            
            DefaultTableModel RecordTableCo = (DefaultTableModel)coachTable.getModel();
            RecordTableCo.setRowCount(0);
            
            while (RS.next()){
                Vector coloumnData =new Vector();
                
                for(int i=0;i<q;i++){
                    coloumnData.add(RS.getString("cid"));
                    coloumnData.add(RS.getString("cname"));
                    coloumnData.add(RS.getString("gender"));
                    coloumnData.add(RS.getString("sname"));
                    coloumnData.add(RS.getString("exp"));
                    coloumnData.add(RS.getString("salary"));
                    
                      
                }
                RecordTableCo.addRow(coloumnData);
            }
            sqlconn.close();
            }
        catch (ClassNotFoundException ex) {
            java.util.logging.Logger.getLogger(coach.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(coach.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
           JOptionPane.showMessageDialog(null, ex);
        }   
        
    }
    
